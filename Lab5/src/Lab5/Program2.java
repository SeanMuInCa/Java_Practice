public class Program2
{
    /**
     * To compute the average of three numbers
     *
     * @param x double first number
     * @param y double second number
     * @param z double third number
     * @return double the average of three params
     */
    public static double average(double x, double y, double z)
    {
        return (x + y + z) / 3;
    }
}
